bicycles = ['trek','cannondale','redline','specialized']
# accessing a element in a list
print (bicycles[0])
# as you can see we retrieved the element "trek" by calling our list and then telling python what we want from the list by its address

# we can also perform methods on elements in a list such as the title method to format our elements in a certain way
# a use for this is when a user enters data such as their name you can use such methods to clean up input
print(bicycles[0].title())